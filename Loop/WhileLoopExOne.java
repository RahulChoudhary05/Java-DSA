// Print number from 1 to 10 using While Loop
import java.util.*;

public class WhileLoopExOne {
    public static void main(String[] args) {
        int counter = 1;
        while (counter <= 10) {
            System.out.println(counter);
            // System.out.print(counter+" ");
            counter++;
        }
        System.out.println("Print 1 to 10 numbers");
    }
}